  // Wait for the DOM to be loaded
  document.addEventListener("DOMContentLoaded", function() {
    // Get all the slide content paragraphs
    var slideContents = document.querySelectorAll(".slide-content");
    
    // Get the slide button
    var slideButton = document.getElementById("slideButton");
    
    // Set initial slide index
    var currentSlide = 0;
    
    // Add click event listener to the button
    slideButton.addEventListener("click", function() {
      // Hide the current slide
      slideContents[currentSlide].classList.remove("active");
      
      // Increment the slide index or reset to 0 if it exceeds the number of slides
      currentSlide = (currentSlide + 1) % slideContents.length;
      
      // Show the next slide
      slideContents[currentSlide].classList.add("active");
    });
  });


